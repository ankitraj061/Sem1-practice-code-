public class A9 {
    public static void main(String[] args) {
        int a[] = { 2, 4, 6, 8 };
        int sum = 0;
        for (int i = 0; i < a.length; i++) {
            sum += a[i];
        }
        System.out.println("Sum of all elements of are : " + sum);

    }

}
